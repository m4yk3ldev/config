  sections = {
    lualine_a = { "mode" },
    lualine_b = { branch, diagnostics, diff },
    lualine_c = { gps },
    lualine_x = { load_lsp, spaces, "encoding", filetype },
    lualine_y = { location },
    lualine_z = { progress },
  },
  inactive_sections = {
    lualine_a = {},
    lualine_b = {},
    lualine_c = { "filename" },
    lualine_x = { "location" },
    lualine_y = {},
    lualine_z = {},
  },
  tabline = {},
  extensions = { "nvim-tree", "fugitive" },
})
